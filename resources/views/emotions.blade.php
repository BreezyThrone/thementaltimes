    <h3>The Negative Impact of Emotions</h3>
    <p>However, emotions can also negatively impact our mental health when they become overwhelming or persist for long periods:</p>
    <ul>
        <li>Feelings of sadness, anxiety, or anger that are difficult to manage</li>
        <li>Emotional exhaustion and burnout</li>
        <li>Difficulty concentrating or making decisions</li>
        <li>Strained relationships and social withdrawal</li>
    </ul>

