    <h3>The Negative Impact of Work Stress</h3>
    <p>However, work can also negatively impact our mental health, especially when it becomes overwhelming:</p>
    <ul>
        <li>Increased anxiety and stress levels</li>
        <li>Feelings of burnout and exhaustion</li>
        <li>Difficulty maintaining work-life balance</li>
        <li>Decreased job satisfaction and motivation</li>
        <li>Physical symptoms such as headaches, muscle tension, and sleep disturbances</li>
    </ul>

